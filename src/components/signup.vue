<script>
import { mapGetters, mapActions } from "vuex";

export default {
  name: "Features",
  data() {
    return {
      svg12a: 0,
      svg12b: 0
    };
  },
  computed: mapGetters([
    "docHeightPerc",
    "sectionsEndingPerc",
    "sectionsClientHeight"
  ]),
  methods: {
    ...mapActions([
      "getDocumentHeightPercentage",
      "getSectionsClientHeight",
      "fillStroke",
      "unfillStroke",
      "setDashArrayAndOffset"
    ]),

    //event on scroll
    handleScroll() {
      this.getDocumentHeightPercentage();
      let start = this.sectionsEndingPerc.section7 - 2;

      //svg handle starts
      if (this.docHeightPerc < start) {
        this.unfillStroke([this.svg12a, this.svg12b]);
      }
      if (this.docHeightPerc >= start) {
        if (this.docHeightPerc <= start + 1) {
          this.drawPath(this.svg12a, start, start + 1);
          this.unfillStroke([this.svg12b]);
        }

        if (
          this.docHeightPerc > start + 1 &&
          this.docHeightPerc <= start + 2.5
        ) {
          this.drawPath(this.svg12b, start + 1, start + 2.5);
          this.fillStroke([this.svg12a]);
        }

        if (this.docHeightPerc > start + 2.5) {
          this.fillStroke([this.svg12a, this.svg12b]);
        }
      }
    },

    //draw line
    drawPath(el, startPerc = 0, endPerc, sign = 1) {
      var drawLen = 0;
      var pathLen = el.getTotalLength();
      var scrollPerc = (this.docHeightPerc - startPerc) / (endPerc - startPerc);
      drawLen = pathLen - pathLen * scrollPerc;
      el.setAttribute("stroke-dashoffset", sign * drawLen);
    }
  },
  mounted() {
    this.getDocumentHeightPercentage();
    window.addEventListener("scroll", this.handleScroll);

    this.svg12a = document.querySelector("#svg_12a");
    this.svg12b = document.querySelector("#svg_12b");

    this.setDashArrayAndOffset([this.svg12a, this.svg12b]);

    this.getSectionsClientHeight({
      sectionId: "section8",
      sectionClientHeight: document.querySelector("#section8").offsetHeight
    });
  },
  destroyed() {
    window.removeEventListener("scroll", this.handleScroll);
  }
};
