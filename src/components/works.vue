<script>
import { mapGetters, mapActions } from "vuex";

export default {
  name: "Works",
  data() {
    return {
      s2path1: 0,
      s2path2: 0,
      svgProgress: 0,
      hours1: 0,
      minutes1: 0,
      euros1: 0,
      hours2: 0,
      minutes2: 0,
      euros2: 0,
      lastScrollTop: 0
    };
  },
  computed: mapGetters([
    "docHeightPerc",
    "sectionsEndingPerc",
    "sectionsClientHeight"
  ]),
  methods: {
    ...mapActions([
      "getDocumentHeightPercentage",
      "getSectionsClientHeight",
      "fillStroke",
      "unfillStroke",
      "setDashArrayAndOffset"
    ]),

    //event on scroll
    handleScroll() {
      this.getDocumentHeightPercentage();

      let start = this.sectionsEndingPerc.section1;

      // handling section2 scroll starts
      if (this.docHeightPerc < start) {
        document.querySelector("#testing").style.position = "relative";
      } else if (this.docHeightPerc >= start) {
        document.querySelector("#testing").style.position = "fixed";
        document.querySelector("#testing").style.transform = `translateY(0px)`;
      }
      if (window.scrollY >= this.sectionsClientHeight.section2) {
        document.querySelector(
          "#testing"
        ).style.transform = `translateY(${(window.scrollY -
          this.sectionsClientHeight.section2) *
          -1}px)`;
      }
      // handling section2 scroll ends

      //svg process starts
      if (this.docHeightPerc < start) {
        this.unfillStroke([this.s2path1, this.s2path2]);
      } else if (
        this.docHeightPerc >= start &&
        this.docHeightPerc <= start + 16
      ) {
        this.drawPath(this.s2path1, start, start + 14);
        this.drawPath(this.s2path2, start, start + 14);

        window.console.log(this.svgStatus(this.s2path1));
        //animatins on svg progress starts
        if (Math.floor(this.svgStatus(this.s2path1)) < 18) {
          document.querySelectorAll(".new-0, .old-0").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 18) {
          document.querySelectorAll(".new-0, .old-0").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 28) {
          document.querySelectorAll(".label").forEach(el => {
            el.style.opacity = "0";
          });

          document
            .querySelector(".counter--a")
            .classList.remove("counter-a-bar");
          document
            .querySelector(".counter--b")
            .classList.remove("counter-b-bar");
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 28) {
          document.querySelector(".label").style.opacity = "1";

          document.querySelector(".counter--a").classList.add("counter-a-bar");
          document.querySelector(".counter--b").classList.add("counter-b-bar");
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 31) {
          document.querySelectorAll("#old_icon-1, #new_icon-1").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 31) {
          document.querySelectorAll("#old_icon-1, #new_icon-1").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 41) {
          document.querySelectorAll("#new_icon-2").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 41) {
          document.querySelectorAll("#new_icon-2").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 54) {
          document.querySelectorAll("#old_icon-2, #new_icon-3").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 54) {
          document.querySelectorAll("#old_icon-2, #new_icon-3").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 80) {
          document.querySelectorAll("#old_icon-3").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 80) {
          document.querySelectorAll("#old_icon-3").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 100) {
          document.querySelectorAll("#old_icon-4").forEach(el => {
            el.style.opacity = "0";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 100) {
          document.querySelectorAll("#old_icon-4").forEach(el => {
            el.style.opacity = "1";
          });
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 33) {
          this.hours1 = 0;
          this.minutes1 = 0;
          this.euros1 = 0;
          this.hours2 = 0;
          this.minutes2 = 0;
          this.euros2 = 0;
        }

        if (
          Math.floor(this.svgStatus(this.s2path1)) >= 33 &&
          Math.floor(this.svgStatus(this.s2path1)) < 100
        ) {
          let time = { ...this.timeConvert(window.scrollY - 1966) };
          this.hours1 = time.hours;
          this.minutes1 = time.minutes;

          if (Math.floor(this.svgStatus(this.s2path1)) < 68) {
            this.hours2 = time.hours;
            this.minutes2 = time.minutes;
          }

          var st = window.pageYOffset || document.documentElement.scrollTop;
          if (st > this.lastScrollTop) {
            // downscroll code
            this.euros1 += 1;
            if (Math.floor(this.svgStatus(this.s2path1)) < 68) {
              this.euros2 += 1;
            }
          } else {
            // upscroll code
            if (this.euros1 > 0) {
              this.euros1 -= 1;
            }
            if (this.euros2 > 0) {
              this.euros2 -= 1;
            }
          }
          this.lastScrollTop = st <= 0 ? 0 : st;
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 68) {
          document.querySelector("#check_b").style.opacity = "0";
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 68) {
          document.querySelector("#check_b").style.opacity = "1";
        }

        if (Math.floor(this.svgStatus(this.s2path1)) < 110) {
          document.querySelector("#check_a").style.opacity = "0";
        }

        if (Math.floor(this.svgStatus(this.s2path1)) >= 110) {
          document.querySelector("#check_a").style.opacity = "1";
        }

        //animatins on svg progress ends

        if (this.docHeightPerc < start + 3.4) {
          let els = document.querySelectorAll(".anim");
          els.forEach(el => {
            el.style.transform = `translateX(0px)`;
          });
        }
        // change 16 to 14 to remove the bug on refresh
        if (
          this.docHeightPerc >= start + 3.4 &&
          this.docHeightPerc <= start + 16
        ) {
          // window.console.log(window.scrollY - 2380);

          let els = document.querySelectorAll(".anim");
          els.forEach(el => {
            el.style.transform = `translateX(${(window.scrollY - 2220) *
              -1}px)`;
          });
        }
        if (this.docHeightPerc <= start + 16) {
          document.querySelector("#product-arrow").style.opacity = "0";
        }
      }
      if (this.docHeightPerc > start + 16) {
        document.querySelector("#product-arrow").style.opacity = "1";
      }
    },

    //draw line
    drawPath(el, startPerc = 0, endPerc, sign = 1) {
      var drawLen = 0;
      var pathLen = el.getTotalLength();
      var scrollPerc = (this.docHeightPerc - startPerc) / (endPerc - startPerc);
      drawLen = pathLen - pathLen * scrollPerc;
      el.setAttribute("stroke-dashoffset", sign * drawLen);
    },

    svgStatus(el) {
      let temp =
        ((el.getTotalLength() - el.getAttribute("stroke-dashoffset")) /
          el.getTotalLength()) *
        100;

      return temp;
    },

    timeConvert(pixels) {
      var num = pixels;
      var hours = num / 60;
      var rhours = Math.floor(hours);
      var minutes = (hours - rhours) * 60;
      var rminutes = Math.round(minutes);
      return {
        minutes: rminutes,
        hours: rhours
      };
    }
  },
  mounted() {
    this.getDocumentHeightPercentage();
    window.addEventListener("scroll", this.handleScroll);

    this.getSectionsClientHeight({
      sectionId: "section2",
      sectionClientHeight: document.querySelector("#section2").offsetHeight
    });

    this.s2path1 = document.querySelector("#s2-path1");
    this.s2path2 = document.querySelector("#s2-path2");

    this.setDashArrayAndOffset([this.s2path1, this.s2path2]);
  },
  destroyed() {
    window.removeEventListener("scroll", this.handleScroll);
  }
};
</script>

